module.exports.add=function add(a,b)
{
    console.log("Addition Is:");
    return a+b;
}
module.exports.sub=function sub(a,b)
{
    console.log("Subtraction Is:");
    return a-b;
}
module.exports.mul=function mul(a,b)
{
    console.log("Multiplication Is:");
    return a*b;
}
module.exports.div=function div(a,b)
{
    console.log("Division Is:");
    return a/b;
}